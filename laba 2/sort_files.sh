#!/bin/bash

# Проверяем, был ли передан аргумент (имя директории)
if [ -z "$1" ]; then
  echo "Ошибка: директория не указана."
  exit 1
fi

# Проверяем, существует ли директория
if [ ! -d "$1" ]; then
  echo "Ошибка: директория '$1' не существует."
  exit 1
fi

# Выводим список файлов, отсортированных по дате модификации (самые новые вверху)
echo "Список файлов в директории '$1', отсортированный по дате модификации:"
ls -lt "$1"



